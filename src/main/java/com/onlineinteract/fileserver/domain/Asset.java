package com.onlineinteract.fileserver.domain;

public class Asset {
	String name;
	String value;
	
	public Asset() {}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
}
